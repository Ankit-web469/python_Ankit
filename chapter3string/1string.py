#slicing
name="ankit"
short1=name[1:5] #exclude the 3 
short2=name[-4:-1] #exclude the 3 
print(name[:4]) #its same as name[0:4]
print(name[1:]) #its same as name[0:5]
print(name[::-1]) #its same as name[0:5]
print("nick name:",short1 )
print("nick name:",short2 )
