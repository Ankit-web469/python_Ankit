st="ankit"
#len function - return length of string 
print(len(st))  
#variable-name.endswith() - tells whether the string end with the string or not 
print(st.endswith("it"))    
#variable-name.endswith() - tells whether the string start with the string or not 
print(st.startswith("an"))  
#variable-name.capitalize- make the first letter capital
print(st.capitalize())
#variable-name.find()- return  index of first occurrence of that word 
print(st.find("k"))
#variable-name.replace()-replace old word with new word
print(st.replace("ankit","an"))
